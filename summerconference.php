			 <h2>TRANSPORTATION INFORMATION</h2>
			 <P>We will offer free shuttle service at 9:30 p.m. on Tuesday July 24th from the Memphis Airport.</P>
			 <P>We will offer free shuttle service at 1:30 p.m. on Friday July 27th back to Memphis.</P>
			 
			 <p><strong>We need confirmation if you plan to use this service. Email Kim Shackelford at <a href="mailto:kshackel@olemiss.edu">kshackel@olemiss.edu</a></strong></p>
			 
			 <P>The Memphis airport is 90 miles from Oxford. There are various rental car agencies but make sure when making your arrangements 
			 that the rental agencies are open late at night for late night flights.</P>
			 
			 <P>Most of the non-overnight flights from Belize land at Memphis after 6 p.m. The non-overnight flights returning Belize leave @ 
			 6:00 a.m. When making arrangements from Belize be aware of overnight stays in Miami, Dallas, Houston, Atlanta or Charlotte. There 
			 are flights to/from Memphis that are one day with shorter layovers in these cities. There are flights that land in time for you to 
			 make the 9:30 pm shuttle. We can’t run a shuttle after that so you will probably need a room in Memphis if you arrive after the 
			 9:30 pm shuttle leaves the airport.</P>
			 
			 <p>We can offer a shuttle at other earlier times in the day including July 24, 25, 26, or 27, but it will be at a cost to the rider.
			 Our individual prices to $60 one-way and $100 round trip per person. Please email <A href="mailto:jcmurphr@olemiss.edu">jcmurphr@olemiss.edu </A>to reserve the shuttle.</p>
			 
