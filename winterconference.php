		  <p style="margin-left: 5px; margin-right: 5px;">The winter 2014 COBEC conference is just around the corner.  Stann Creek Ecumenical Junior 
College in <b>Dangriga</b> will be serving as our Belize hosts.  Both Chairs are already getting inquiries, 
which is great, as to when and where the conference will be.  As usual, the College Fair will be in 
