PM. This will be following the Library RDA workshops that will begin the last week of January.  
The conference itself will be held in Dangriga on Thursday, February 6th and Friday, February 7th.  
Dr. Karen Martinez will be providing us with more information regarding lodging and so on.  In 
addition to the Pelican Beach Resort, we also have the Jungle Huts, Pal’s Guest House, and the 
Chaleanor Hotel in the Dangriga area.  Lodging and a conference site in Hopkins is also being 
considered.  And to our U.S. partners, if you have never been to Dangriga or Hopkins before, you 
