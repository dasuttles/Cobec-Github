						<h2 class="expand"><span class="style5">&raquo; </span>Membership</h2>
						<div class="collapse">
							<p><strong>Membership to COBEC is open to the following:</strong></p>
							<ul><li>Belizean post-secondary educational institutions.</li>
							<li>Belizean governmental and private organizations and agencies involved in higher education .</li>
							<li>Non-Belizean organization, agencies, and institutions actively interested in higher education in Belize.</li></ul>
							<strong>
							<p>Non-Belizean members seeking membership into COBEC, please fill out the <a href="documents/COBEC_Membership_Application.doc">Membership Application for non-Belizean Members</a> and send it to the COBEC US Co-Chair.</p>
							</strong>
